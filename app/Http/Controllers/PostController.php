    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $post = new Post();
        $post->user_id = Auth::user()->id;
        $post->title = $request->title;
        $post->comment = $request->comment;
        $post->save();

        return redirect('/post')->with('success', '投稿しました。');
    }

